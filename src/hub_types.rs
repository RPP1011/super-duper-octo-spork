/// Persistent UI state for the hero detail panel shown in GuildManagement.
#[derive(Resource, Default)]
pub struct HeroDetailUiState {
    /// The hero id currently selected in the detail panel.  `None` = first hero.
    pub selected_hero_id: Option<u32>,
    /// A loot item generated by "Generate Test Item" that is waiting to be equipped.
    pub pending_loot: Option<game_core::EquipmentItem>,
    /// Tracks an internal seed counter so repeated button presses yield different items.
    pub loot_seed_counter: u64,
}
